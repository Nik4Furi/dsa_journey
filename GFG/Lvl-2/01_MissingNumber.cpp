class Solution{
  public:
    int missingNumber(vector<int>& array, int n) {
        // Your code goes here
        
            int ans = 1;
            
            for(int i=2;i<=n;i++) ans ^= i;
            for(auto x:array) ans ^= x;
            
            return ans;
    }
};

//{ Driver Code Starts.

int main() {
    int t;
    cin >> t;
    while (t--) {
        int n;
        cin >> n;

        vector<int> array(n - 1);
        for (int i = 0; i < n - 1; ++i) cin >> array[i];
        Solution obj;
        cout << obj.missingNumber(array, n) << "\n";
    }
    return 0;
}
